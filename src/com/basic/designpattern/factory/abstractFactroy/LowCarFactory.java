package com.basic.designpattern.factory.abstractFactroy;

public class LowCarFactory implements CarFactroy {
    @Override
    public Engine createEngine() {
        return new LowEngine();
    }

    @Override
    public Seat createSeat() {
        return new LowSeat();
    }

    @Override
    public Tyre createTyre() {
        return new LowTyre();
    }
}
